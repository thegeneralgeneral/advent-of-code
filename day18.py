ON = '#'
OFF = '.'

class Light(object):
    def __init__(self, x, y, value=None):
        self.x = x
        self.y = y
        self.coords = (x, y)
        self.value = value
    
    @property
    
    def __str__(self):
        return "L(%s, %s): <%s>" % (self.x, self.y, self.value)
    
    def __repr__(self):
        return str(self)

class Grid(object):
    
    def __init__(self, width, height, list_of_on_lights):
        self.width = width
        self.height = height
        self.lights = {}
        for x in range(0, width):
            self.lights[x] = {}
            for y in range(0, height):
                value = ON if (x, y) in list_of_on_lights else OFF
                self.lights[x][y] = Light(x, y, value)
    
    def __str__(self):
        rows = []
        for y in range(self.height):
            rows.append(''.join([str(self.lights[x][y].value) for x in range(0, self.width)]))
        result = '\n'.join(rows)
        return result
    
    def set_corners_on(self):
        self.lights[0][0].value = ON
        self.lights[0][self.height-1].value = ON
        self.lights[self.width-1][0].value = ON
        self.lights[self.width-1][self.width-1].value = ON

    def get_neighbours(self, x, y):
        result = []
        
        min_x, max_x = max(0, x-1), min(self.width - 1, x+1)
        min_y, max_y = max(0, y-1), min(self.height - 1, y+1)
        
        for i_x in range(min_x, max_x + 1):
            for i_y in range(min_y, max_y + 1):
                if not (i_x, i_y) == (x, y):
                    result.append(self.lights[i_x][i_y])
        return set(result)

    def get_next_cell_state(self, light):
        neighbours = self.get_neighbours(light.x, light.y)
        num_on_neighbours = len([l for l in neighbours if l.value == ON])
        if light.value == ON:
            if num_on_neighbours in [2, 3]:
                # print 'On staying On'
                return ON
            else:
                # print 'On turning Off'
                return OFF
        else:
            if num_on_neighbours == 3:
                # print 'Off turning On'
                return ON
            else:
                # print 'Off staying Off'
                return OFF

    def step(self):
        new_lights_grid = {}
        for x in range(0, self.width):
            new_lights_grid[x] = {}
            for y in range(0, self.height):
                new_value = self.get_next_cell_state(self.lights[x][y])
                new_lights_grid[x][y] = Light(x, y, new_value)
        self.lights = new_lights_grid
    
    def step_with_stuck_lights(self):
        new_lights_grid = {}
        self.set_corners_on()
        self.step()
        self.set_corners_on()
    
    def get_num_lights(self):
        total = 0
        for x in range(0, self.width):
            for y in range(0, self.height):
                if self.lights[x][y].value == ON:
                    total += 1
        return total


def convert_string_to_grid(input_string):
    rows = input_string.split('\n')
    height = len(rows)
    width = len(rows[0])
    list_of_on_lights = []
    for i, row in enumerate(rows):
        for j in range(0, len(row)):
            if row[j] == '#':
                list_of_on_lights.append((j, i))
    return Grid(height, width, list_of_on_lights)


